package com.github.dreamhead.bot;

public class BigData {
    private Data data;
    private int intValue;

    private Integer integerValue;

    private double doubleValue;

    private Double aDoubleValue;

    private long longValue;

    private Long aLongValue;

    private float floatValue;

    private Float aFloatValue;

    private char charValue;

    private Character characterValue;

    private short shortValue;

    private byte byteValue;

    private boolean booleanValue;

    public BigData(final Data data) {
        this.data = data;
    }

    public Data getData() {
        return data;
    }

    public int getIntValue() {
        return intValue;
    }

    public Integer getIntegerValue() {
        return integerValue;
    }

    public double getDoubleValue() {
        return doubleValue;
    }

    public Double getADoubleValue() {
        return aDoubleValue;
    }

    public long getLongValue() {
        return longValue;
    }

    public Long getALongValue() {
        return aLongValue;
    }

    public float getFloatValue() {
        return floatValue;
    }

    public Float getAFloatValue() {
        return aFloatValue;
    }

    public char getCharValue() {
        return charValue;
    }

    public Character getCharacterValue() {
        return characterValue;
    }

    public short getShortValue() {
        return shortValue;
    }

    public byte getByteValue() {
        return byteValue;
    }

    public boolean isBooleanValue() {
        return booleanValue;
    }
}
